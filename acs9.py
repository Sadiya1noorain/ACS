import math
a = 22/7
b = 2
print('The ceil of ', a, 'is : ', math.ceil(a))
print('The floor of ', a, 'is : ', math.floor(a))
print('The fabs of ', a, 'is : ', math.fabs(a))
print('The value of ', a, 'raised to the power ', b, 'is : ', math.pow(a, b)) 
print('The sqrt of ', a, 'is : ', math.sqrt(a))
print('The exponential of ', b, 'is : ', math.exp(b))
